package Enum;

/**
 * Enum de direction possible pour les déplacements
 */
public enum EDirection {
    /**
     * Direction pour monter
     */
    Haut,
    /**
     * Direction pour descendre
     */
    Bas,
    /**
     * Direction pour aller à gauche
     */
    Gauche,
    /**
     * Direction pour aller à droite
     */
    Droite,
}
